function func = func_r2r1(d)
% r2r1-Ma1关系方程
r = 1.4;
func =@(ma1,Rho) tand(d).^2-(ma1.^2.*(2*Rho./((r+1)*ma1.^2-Rho.*(r-1).*ma1.^2))-1).^2.*...
    (1-(2.*Rho./((r+1).*ma1.^2-Rho.*(r-1).*ma1.^2)))./...
    (1+ma1.^2.*(r./2+0.5-(2.*Rho./((r+1).*ma1.^2-Rho.*(r-1).*ma1.^2)))).^2./...
    (2.*Rho./((r+1).*ma1.^2-Rho.*(r-1).*ma1.^2));
end

